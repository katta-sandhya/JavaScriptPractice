// Define an object
const myObject = {
    // Define a function as a property of the object
    myFunction () {
      console.log("Hello from myFunction!");
    },
  
    // You can also define functions using the shorthand notation
    anotherFunction() {
      console.log("Hello from anotherFunction!");
    }
  };
  
  // Call the functions stored in the object
  myObject.myFunction(); // Output: Hello from myFunction!
  myObject.anotherFunction(); // Output: Hello from anotherFunction!
  